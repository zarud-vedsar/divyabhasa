// swal alert start
const Toast = Swal.mixin({
    toast: true,
    position: 'top-right',
    iconColor: 'white',
    customClass: {
        popup: 'colored-toast'
    },
    showConfirmButton: false,
    timer: 1500,
    timerProgressBar: true
});
const swamsg = {
    error: (msg) => {
        Toast.fire({
            icon: 'error',
            title: msg
        });
    },
    success: (msg) => {
        Toast.fire({
            icon: 'success',
            title: msg
        })
    }
};
// swal alert end 

// delete function start
function deleteData(query) {
    $(document).on('click', query, function (e) {
        e.preventDefault();
        const ref_id = $(this).data('ref_id');
        const collection = $(this).data('collection');
        const remove = $(this).data('remove');
        let files = $(this).data('files') || '';

        Swal.fire({
            title: "Are you sure?",
            text: "You won't be able to revert this!",
            icon: "warning",
            showCancelButton: true,
            confirmButtonColor: "#3085d6",
            cancelButtonColor: "#d33",
            confirmButtonText: "Yes, delete it!"
        }).then((result) => {
            if (result.isConfirmed) {
                $.ajax({
                    url: './inc/config/delete.php',
                    type: 'POST',
                    data: { ref_id, collection, files },
                    success: response => {
                        const res = JSON.parse(response);
                        if (res.status == 100) {
                            swamsg.success('Deleted !!');
                            $(remove).fadeOut(300, () => {
                                $(remove).remove();
                            })
                        } else {
                            swamsg.error('Failed !!');
                        }
                    }
                });
            } else {
                swamsg.success('Your data is safe !');
            };
        });
    });
};
//delete function end

// Status function start
function changeStatus(query) {
    $(document).on('click', query, function (e) {
        e.preventDefault();
        const element = $(this);
        const ref_id = element.data('ref_id');
        const collection = element.data('collection');
        const status = element.prop('checked') ? 1 : 0;

        $.ajax({
            url: './inc/config/status.php',
            type: 'POST',
            data: { status, ref_id, collection },
            success: response => {
                const res = JSON.parse(response);
                if (res.status == 100) {
                    swamsg.success(res.msg);
                    element.prop('checked', !!status);
                } else if (res.status == 101) {
                    swamsg.error(res.msg);
                }
            }
        });
    });
};
//Status function end

